use crate::{mesh::StaticMesh, physics::PhysicalState};

////////////////////////////////////////////////////////////////////////////////
///Types & Traits //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

pub struct WorldObject {
    mesh: StaticMesh,

    pub physics: PhysicalState,
}

////////////////////////////////////////////////////////////////////////////////
/// Implementations ////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

impl Default for WorldObject {
    fn default() -> Self {
        let mut mesh = StaticMesh::default();
        mesh.load_cube(100);

        Self {
            mesh,
            physics: Default::default(),
        }
    }
}
